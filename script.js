let currentIndex = new Array(products.length).fill(0);

function nextImage(productNumber){
    currentIndex[productNumber]++;

    if(currentIndex[productNumber] >= products[productNumber].length){
        currentIndex[productNumber]=0;
    }

    document.getElementById("img"+productNumber).src =
        products[productNumber][currentIndex[productNumber]];
}

function searchProducts(){
    let input=document.getElementById("search").value.toLowerCase();
    let items=document.getElementsByClassName("product");

    for(let i=0;i<items.length;i++){
        let name=items[i]
            .getElementsByClassName("product-name")[0]
            .innerText.toLowerCase();

        items[i].style.display=name.includes(input)?"":"none";
    }
}